#include "libft.h"

char	*ft_strcpy(char *d, const char *s)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		d[i] = s[i];
		if (s[i] == '\0')
			break ;
		i++;
	}
	d[i] = '\0';
	return (d);
}
