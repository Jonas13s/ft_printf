#include "libft.h"

char	*ft_strncpy(char *d, const char *s, size_t n)
{
	size_t	i;
	int		on;

	on = 0;
	i = 0;
	while (i < n)
	{
		if (on == 0)
			d[i] = s[i];
		else
			d[i] = '\0';
		if (s[i] == '\0')
			on = 1;
		i++;
	}
	return (d);
}
